public class IfElseDemo {
    public static void main(String[] args) {
        int month = 2;
        if (month == 1) {
            System.out.println("Có 31 ngày");
        } else if (month == 3) {
            System.out.println("Có 31 ngày");
        } else {
            System.out.println("Không biết");
        }
    }
}
